#include "minishell.h"

int	ft_env(t_shell *shell)
{
	int	i;

	i = 0;
	if (shell->env)
	{
		while (shell->env[i])
		{
			ft_printf("%s\n", shell->env[i]);
			i++;
		}
	}
	exit (0);
}